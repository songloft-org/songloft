func tagsUnchanged(filePath string, opts tag.WriteOptions) (bool, string) {
	f, err := os.Open(filePath)
	if err != nil {
		return false, "open failed"
	}
	defer f.Close()

	existing, err := tag.ReadFrom(f)
	if err != nil {
		return false, "read existing tags failed"
	}

	if existing.Title() != opts.Title {
		return false, "title differs"
	}
	if existing.Artist() != opts.Artist {
		return false, "artist differs"
	}
	if existing.Album() != opts.Album {
		return false, "album differs"
	}
	if existing.Genre() != opts.Genre {
		return false, "genre differs"
	}
	if existing.Lyrics() != opts.Lyrics {
		return false, "lyrics differs"
	}
	// Year: 0 ??"???",??????????????
	if opts.Year > 0 && existing.Year() > 0 && existing.Year() != opts.Year {
		return false, "year differs"
	}

	return true, "all fields match"
}

func WriteSongTags(filePath string, song *models.Song) FileWriteStatus {
	if filePath == "" || song == nil {
		return FileWriteUnchanged
	}

	// song.Lyric ? LyricPayload JSON;tag ???? LRC ??,??????
	mainLyric := models.UnmarshalLyric(song.Lyric).Lyric

	opts := tag.WriteOptions{
		Title:       song.Title,
		Artist:      song.Artist,
		AlbumArtist: song.Artist, // ?????????????????
		Album:       song.Album,
		Lyrics:      mainLyric,
	}

	if song.Year > 0 {
		opts.Year = song.Year
	} else if !song.AddedAt.IsZero() {
		opts.Year = song.AddedAt.Year()
	}
	opts.Genre = song.Genre

	// ?????:lyric_source=url ?????????
	if song.LyricSource == models.LyricSourceURL {
		opts.Lyrics = ""
	}

	// ????(?? cover_path ????)
	if song.CoverPath != "" {
		if data, err := os.ReadFile(song.CoverPath); err == nil {
			opts.Picture = &tag.Picture{
				MIMEType:    tag.MIMETypeFromExt(filepath.Ext(song.CoverPath)),
				Data:        data,
				Description: "",
			}
		} else {
			slog.Debug("read cover failed, skip embedding",
				"coverPath", song.CoverPath, "error", err)
		}
	}

	// ???????????,????????????
	if unchanged, reason := tagsUnchanged(filePath, opts); unchanged {
		slog.Debug("tag unchanged, skip write", "path", filePath, "reason", reason)
		return FileWriteUnchanged
	}

	if err := tag.WriteTag(filePath, opts); err != nil {
		if errors.Is(err, tag.ErrUnsupportedWrite) {
			slog.Debug("tag write skipped for unsupported format",
				"path", filePath, "error", err)
			return FileWriteUnchanged
		}
		slog.Warn("write tag failed", "path", filePath, "error", err)
		return FileWriteFailed
	}

	slog.Debug("tag written", "path", filePath,
		"title", opts.Title, "artist", opts.Artist,
		"hasPicture", opts.Picture != nil,
		"lyricsLen", len(opts.Lyrics))
	return FileWriteWritten
}
